    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        double in, out, hoursworked, salary = 0;
        double tohour = 1000 * 60 * 60;
        try (java.sql.Connection con = java.sql.DriverManager.getConnection(Externs.host, Externs.username, Externs.password)) {
            String SQL = "UPDATE ATTENDANCE SET LOGOUT = CURRENT_TIME, LOGINFLAG = 0 WHERE EMPLOYEENAME = '" + jComboBox1.getSelectedItem() + "'";
            java.sql.PreparedStatement pst = con.prepareStatement(SQL);
            pst.executeUpdate();
            SQL = "SELECT * FROM ATTENDANCE WHERE EMPLOYEENAME = '" + jComboBox1.getSelectedItem() + "'";
            pst = con.prepareStatement(SQL);
            java.sql.ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                in = rs.getTime("LOGIN").getTime() / tohour;
                out = rs.getTime("LOGOUT").getTime() / tohour;
                hoursworked = out - in;
                salary = ratePerHour((String) jComboBox1.getSelectedItem()) * hoursworked;
            }
            SQL = "UPDATE ATTENDANCE SET SALARY = " + salary
                    + "WHERE EMPLOYEENAME = '" + jComboBox1.getSelectedItem() + "'";
            pst = con.prepareStatement(SQL);
            pst.executeUpdate();
            con.close();
        } catch (java.sql.SQLException err) {
            javax.swing.JOptionPane.showMessageDialog(null, err);
        }
        jButton1.setEnabled(false);
        jButton2.setEnabled(false);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        int loginflag = 0;
        try (java.sql.Connection con = java.sql.DriverManager.getConnection(Externs.host, Externs.username, Externs.password)) {
            String SQL = "SELECT * FROM ATTENDANCE WHERE EMPLOYEENAME = '" + jComboBox1.getSelectedItem() + "'";
            java.sql.PreparedStatement pst = con.prepareStatement(SQL);
            java.sql.ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                loginflag = rs.getInt("LOGINFLAG");
            }
            if (loginflag == 0) {
                jButton1.setEnabled(true);
                jButton2.setEnabled(false);
            } else {
                jButton1.setEnabled(false);
                jButton2.setEnabled(true);
            }
            con.close();
        } catch (java.sql.SQLException err) {
            javax.swing.JOptionPane.showMessageDialog(null, err);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

