    private void employeeid() {
        try (java.sql.Connection con = java.sql.DriverManager.getConnection(Externs.host, Externs.username, Externs.password)) {
            String SQL = "SELECT * FROM PERSONS WHERE TYPE = 0 AND ISDELETED = 0";
            java.sql.PreparedStatement pst = con.prepareStatement(SQL);
            java.sql.ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                jComboBox1.addItem(rs.getString("IDNUMBER"));
            }
            con.close();
        } catch (java.sql.SQLException err) {
            javax.swing.JOptionPane.showMessageDialog(null, err);
        }
    }

    private double ratePerHour(String employeeID) {
        double rateperhour = 0;
        int department = -1;
        try (java.sql.Connection con = java.sql.DriverManager.getConnection(Externs.host, Externs.username, Externs.password)) {
            String SQL = "SELECT * FROM PERSONS WHERE TYPE = 0 AND ISDELETED = 0 AND IDNUMBER = '" + employeeID + "'";
            java.sql.PreparedStatement pst = con.prepareStatement(SQL);
            java.sql.ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                department = 3 + rs.getInt("DEPARTMENT");
            }
            SQL = "SELECT * FROM RATEPERHOUR WHERE ID = " + department + "";
            pst = con.prepareStatement(SQL);
            rs = pst.executeQuery();
            while (rs.next()) {
                rateperhour = rs.getDouble("RATE");
            }
            con.close();
        } catch (java.sql.SQLException err) {
            javax.swing.JOptionPane.showMessageDialog(null, err);
        }
        return rateperhour;
    }

