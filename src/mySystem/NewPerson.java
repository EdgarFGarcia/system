    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        // TODO add your handling code here:
        javax.swing.JFileChooser fileopen = new javax.swing.JFileChooser();
        javax.swing.filechooser.FileFilter filter = new javax.swing.filechooser.FileNameExtensionFilter("Pictures", "jpg");
        fileopen.setFileFilter(filter);
        int ret = fileopen.showDialog(null, "Choose");
        if (ret == javax.swing.JFileChooser.APPROVE_OPTION) {
            java.io.File file = fileopen.getSelectedFile();
            String filepath = "" + file;
            imagepath = "";
            for (int i = 0; i < filepath.length(); i++) {
                char path = filepath.charAt(i);
                if (path == '\\') {
                    imagepath = imagepath + path + '\\';
                } else {
                    imagepath = imagepath + path;
                }
            }
            jLabel2.setText("");
            java.awt.Image image = java.awt.Toolkit.getDefaultToolkit().getImage(imagepath);
            jLabel2.setIcon(new javax.swing.ImageIcon(image.getScaledInstance(jLabel2.getWidth(), jLabel2.getHeight(), java.awt.Image.SCALE_DEFAULT)));
        }
    }//GEN-LAST:event_jLabel2MouseClicked

