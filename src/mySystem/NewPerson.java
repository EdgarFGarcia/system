        // TODO add your handling code here:
        jButton1.setCursor(new java.awt.Cursor(java.awt.Cursor.WAIT_CURSOR));
        
        int count = 0;
        String passKEY = "", userKEY = "";
        int department = 3;
        if ("".equals(jTextField4.getText())) {
            jTextField4.setBackground(java.awt.Color.red);
            count++;
        }
        if ("".equals(jTextField6.getText())) {
            jTextField6.setBackground(java.awt.Color.red);
            count++;
        }
        if ((jComboBox2.getSelectedIndex() == 0) && (jComboBox3.getSelectedIndex()) < 2) {
            department = jComboBox3.getSelectedIndex();
            String initialPasskey = Externs.initialPasskey().toUpperCase();
            passKEY = Externs.passkey(initialPasskey);
            userKEY = Externs.employeeid(jTextField6.getText());
            jTextField3.setText(userKEY);
            jTextField1.setText(initialPasskey);
            System.out.println(userKEY + ", " + passKEY);
        }
        if (count > 0) {
            javax.swing.JOptionPane.showMessageDialog(null, "fields in red are required" + ".", "Input Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        } else {
            java.text.DateFormat dateFormat = new java.text.SimpleDateFormat("yyyy-MM-dd");
            String birthdate = "" + dateFormat.format(jDateChooser1.getDate());
            try (java.sql.Connection con = java.sql.DriverManager.getConnection(Externs.host, Externs.username, Externs.password)) {
                String SQL = "INSERT INTO PERSONS (FIRSTNAME, LASTNAME, BIRTHDATE, IDNUMBER, EMAIL, PHONE, ADDRESS, IMAGE, TYPE, DEPARTMENT, USERKEY, PASSKEY, FAILEDLOGIN, ISLOGIN, ISDELETED, isupdate) VALUES ('"
                        + jTextField4.getText() + "', '"
                        + jTextField6.getText() + "', '"
                        + birthdate + "', '"
                        + Externs.employeeid(jTextField6.getText()) + "', '"
                        + jTextField8.getText() + "', '"
                        + jTextField5.getText() + "', '"
                        + jTextField7.getText() + "', '"
                        + imagepath + "', "
                        + jComboBox2.getSelectedIndex() + ", "
                        + department + ", '"
                        + userKEY + "', '"
                        + passKEY + "', "
                        + "0,0,0,0)";
                java.sql.PreparedStatement pst = con.prepareStatement(SQL);
                pst.executeUpdate();
                javax.swing.JOptionPane.showMessageDialog(null, "Registration successful.");
                con.close();
            } catch (java.sql.SQLException err) {
                javax.swing.JOptionPane.showMessageDialog(null, err);
            }
        }
        jButton1.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
